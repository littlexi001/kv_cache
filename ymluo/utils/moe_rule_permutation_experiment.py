@dataclass
class RuleBatch:
    source: torch.Tensor
    target: torch.Tensor
    metadata: torch.Tensor


class RulePermutationData(Dataset):
    """Causal-LM data where local transitions are controlled by latent rule groups.

    Each block is generated by repeatedly applying a permutation rule:

        x[t + 1] = P_rule(x[t])

    The training target remains the real next token in the generated sequence.
    Metadata is used only for routing diagnostics or optional oracle routing.
    """

    def __init__(
        self,
        max_seq_len: int,
        num_samples: int,
        block_len: int,
        vocab_size: int,
        num_high_units: int,
        num_rule_groups: int,
        num_rules_per_group: int,
        seed: int,
        min_token_id: int = 1,
        pad_token_id: int = 0,
        slot_assignment: str = "mod",
        rule_per_occurrence: str = "slot_fixed",
        start_mode: str = "slot_fixed",
    ) -> None:
        if block_len < 2:
            raise ValueError("block_len must be >= 2.")
        if vocab_size < 2:
            raise ValueError("vocab_size must be >= 2.")
        if num_high_units < 1 or num_rule_groups < 1 or num_rules_per_group < 1:
            raise ValueError("num_high_units, num_rule_groups, and num_rules_per_group must be positive.")
        if slot_assignment not in {"mod", "random"}:
            raise ValueError("slot_assignment must be 'mod' or 'random'.")
        if rule_per_occurrence not in {"slot_fixed", "random_within_group"}:
            raise ValueError("rule_per_occurrence must be 'slot_fixed' or 'random_within_group'.")
        if start_mode not in {"slot_fixed", "random"}:
            raise ValueError("start_mode must be 'slot_fixed' or 'random'.")

        self.max_seq_len = int(max_seq_len)
        self.num_samples = int(num_samples)
        self.block_len = int(block_len)
        self.vocab_size = int(vocab_size)
        self.num_high_units = int(num_high_units)
        self.num_rule_groups = int(num_rule_groups)
        self.num_rules_per_group = int(num_rules_per_group)
        self.num_rules = self.num_rule_groups * self.num_rules_per_group
        self.seed = int(seed)
        self.min_token_id = int(min_token_id)
        self.pad_token_id = int(pad_token_id)
        self.slot_assignment = str(slot_assignment)
        self.rule_per_occurrence = str(rule_per_occurrence)
        self.start_mode = str(start_mode)

        setup_generator = torch.Generator(device="cpu").manual_seed(self.seed)
        if self.slot_assignment == "mod":
            self.slot_to_group = torch.remainder(torch.arange(self.num_high_units), self.num_rule_groups).long()
        else:
            self.slot_to_group = torch.randint(
                0,
                self.num_rule_groups,
                (self.num_high_units,),
                generator=setup_generator,
            ).long()
        slot_rule_offset = torch.div(torch.arange(self.num_high_units), self.num_rule_groups, rounding_mode="floor")
        slot_rule_offset = torch.remainder(slot_rule_offset, self.num_rules_per_group)
        self.slot_to_rule = self.slot_to_group * self.num_rules_per_group + slot_rule_offset

        self.slot_start_offsets = torch.randint(
            0,
            self.vocab_size,
            (self.num_high_units,),
            generator=setup_generator,
        ).long()
        self.permutations = torch.stack(
            [torch.randperm(self.vocab_size, generator=setup_generator) for _ in range(self.num_rules)],
            dim=0,
        ).long()

    def __len__(self) -> int:
        return self.num_samples

    def _item_generator(self, idx: int) -> torch.Generator:
        return torch.Generator(device="cpu").manual_seed(self.seed + 1_000_003 * int(idx))

    def _sample_rule_id(self, slot_id: int, group_id: int, generator: torch.Generator) -> int:
        if self.rule_per_occurrence == "slot_fixed":
            return int(self.slot_to_rule[slot_id].item())
        offset = int(torch.randint(0, self.num_rules_per_group, (1,), generator=generator).item())
        return int(group_id * self.num_rules_per_group + offset)

    def _sample_start_offset(self, slot_id: int, generator: torch.Generator) -> int:
        if self.start_mode == "slot_fixed":
            return int(self.slot_start_offsets[slot_id].item())
        return int(torch.randint(0, self.vocab_size, (1,), generator=generator).item())

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        generator = self._item_generator(idx)
        tokens: list[int] = []
        metadata_rows: list[list[int]] = []

        while len(tokens) < self.max_seq_len + 1:
            slot_id = int(torch.randint(0, self.num_high_units, (1,), generator=generator).item())
            group_id = int(self.slot_to_group[slot_id].item())
            rule_id = self._sample_rule_id(slot_id, group_id, generator)
            offset = self._sample_start_offset(slot_id, generator)
            permutation = self.permutations[rule_id]
            for block_pos in range(self.block_len):
                tokens.append(self.min_token_id + int(offset))
                metadata_rows.append(
                    [
                        slot_id,
                        group_id,
                        rule_id,
                        block_pos,
                        1 if block_pos < self.block_len - 1 else 0,
                    ]
                )
                offset = int(permutation[offset].item())
                if len(tokens) >= self.max_seq_len + 1:
                    break

        source = torch.tensor(tokens[: self.max_seq_len], dtype=torch.long)
        target = torch.tensor(tokens[1 : self.max_seq_len + 1], dtype=torch.long)
        metadata = torch.tensor(metadata_rows[: self.max_seq_len], dtype=torch.long)
        return source, target, metadata


