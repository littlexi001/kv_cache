def sample_batch(dataset: RulePermutationData, args: argparse.Namespace, generator: torch.Generator, device: torch.device) -> RuleBatch:
    indices = torch.randint(0, len(dataset), (args.batch_size,), generator=generator)
    items = [dataset[int(index)] for index in indices.tolist()]
    return RuleBatch(
        source=torch.stack([item[0] for item in items]).to(device),
        target=torch.stack([item[1] for item in items]).to(device),
        metadata=torch.stack([item[2] for item in items]).to(device),
    )


def rule_valid_mask(metadata: torch.Tensor) -> torch.Tensor:
    return metadata[..., RULE_METADATA_FIELDS["valid_rule_transition"]] > 0


def compute_prediction_loss(
    logits: torch.Tensor,
    target: torch.Tensor,
    metadata: torch.Tensor,
    args: argparse.Namespace,
) -> tuple[torch.Tensor, int, int]:
    flat_loss = F.cross_entropy(
        logits.reshape(-1, logits.shape[-1]).float(),
        target.reshape(-1),
        reduction="none",
    ).reshape_as(target)
    pred = logits.argmax(dim=-1)
    if args.loss_rule_positions_only:
        mask = rule_valid_mask(metadata)
    else:
        mask = torch.ones_like(target, dtype=torch.bool)
    count = int(mask.sum().detach().cpu())
    if count == 0:
        return flat_loss.mean(), 0, 0
    loss = flat_loss[mask].mean()
    correct = int(((pred == target) & mask).sum().detach().cpu())
    return loss, correct, count


def same_feature_same_expert_rate(
    labels: torch.Tensor | None,
    feature_ids: torch.Tensor,
    valid_mask: torch.Tensor | None = None,
) -> float | None:
    if labels is None:
        return None
    primary = labels[..., 0] if labels.shape[-1] != feature_ids.shape[-1] else labels
    if primary.dim() == 3:
        rates = []
        for head_idx in range(primary.shape[2]):
            rate = same_feature_same_expert_rate(primary[:, :, head_idx], feature_ids, valid_mask)
            if rate is not None:
                rates.append(rate)
        if not rates:
            return None
        return float(sum(rates) / len(rates))

    same_feature = feature_ids[:, :, None] == feature_ids[:, None, :]
    valid = same_feature & (feature_ids[:, :, None] >= 0) & (feature_ids[:, None, :] >= 0)
    not_self = ~torch.eye(feature_ids.shape[1], dtype=torch.bool, device=feature_ids.device)[None, :, :]
    valid = valid & not_self
    if valid_mask is not None:
        pair_mask = valid_mask[:, :, None] & valid_mask[:, None, :]
        valid = valid & pair_mask
    if not bool(valid.any()):
        return None
    same_expert = primary[:, :, None] == primary[:, None, :]
    return float((same_expert & valid).float().sum().detach().cpu() / valid.float().sum().detach().cpu().clamp_min(1.0))


def attention_history_mass(attn: torch.Tensor, feature_ids: torch.Tensor, valid_mask: torch.Tensor | None = None) -> float | None:
    batch, heads, seq_len, _ = attn.shape
    history = torch.tril(torch.ones(seq_len, seq_len, dtype=torch.bool, device=attn.device), diagonal=-1)
    same = feature_ids[:, :, None] == feature_ids[:, None, :]
    valid = (feature_ids[:, :, None] >= 0) & (feature_ids[:, None, :] >= 0)
    mask = same & valid & history[None, :, :]
    if valid_mask is not None:
        mask = mask & valid_mask[:, :, None] & valid_mask[:, None, :]
    valid_rows = mask.any(dim=-1)
    if not bool(valid_rows.any()):
        return None
    mass = (attn.float() * mask[:, None, :, :].float()).sum(dim=-1)
    row_mask = valid_rows[:, None, :].expand(batch, heads, seq_len)
    return float(mass[row_mask].mean().detach().cpu())


def forced_ids_from_metadata(batch: RuleBatch, args: argparse.Namespace, num_experts: int, mode: str | None = None) -> torch.Tensor | None:
    routing_mode = args.routing_override if mode is None else mode
    if routing_mode == "none":
        return None
    if routing_mode == "all_to_one":
        expert_id = int(args.all_to_one_expert) % int(num_experts)
        return torch.full_like(batch.metadata[..., 0], expert_id)
    if routing_mode == "rule_group":
        return torch.remainder(batch.metadata[..., RULE_METADATA_FIELDS["rule_group"]], int(num_experts)).long()
    if routing_mode == "rule_id_mod":
        return torch.remainder(batch.metadata[..., RULE_METADATA_FIELDS["rule_id"]], int(num_experts)).long()
    raise ValueError(f"Unsupported routing mode: {routing_mode}")


def aggregate_eval_metrics(rows: list[dict[str, Any]]) -> dict[str, Any]:
    total_loss_sum = sum(row["loss_sum"] for row in rows)
    total_count = sum(row["count"] for row in rows)
    total_correct = sum(row["correct"] for row in rows)
    layers: dict[str, dict[str, list[Any]]] = {}
    for row in rows:
        for layer_idx, layer_metrics in row["layers"].items():
            bucket = layers.setdefault(layer_idx, {})
            for key, value in layer_metrics.items():
                if value is not None:
                    bucket.setdefault(key, []).append(value)
    layer_summary: dict[str, dict[str, Any]] = {}
    for layer_idx, values_by_key in layers.items():
        layer_summary[layer_idx] = {}
        for key, values in values_by_key.items():
            first = values[0]
            if isinstance(first, list):
                width = len(first)
                layer_summary[layer_idx][key] = [
                    sum(float(value[idx]) for value in values) / len(values)
                    for idx in range(width)
                ]
            else:
                layer_summary[layer_idx][key] = sum(float(value) for value in values) / len(values)
    mean_layers: dict[str, Any] = {}
    for layer_values in layer_summary.values():
        for key, value in layer_values.items():
            mean_layers.setdefault(key, []).append(value)
    averaged_mean_layers: dict[str, Any] = {}
    for key, values in mean_layers.items():
        first = values[0]
        if isinstance(first, list):
            width = len(first)
            averaged_mean_layers[key] = [
                sum(float(value[idx]) for value in values) / len(values)
                for idx in range(width)
            ]
        else:
            averaged_mean_layers[key] = sum(float(value) for value in values) / len(values)
    loss = total_loss_sum / max(total_count, 1)
    return {
        "loss": loss,
        "accuracy": total_correct / max(total_count, 1),
        "ppl": math.exp(min(loss, 80.0)),
        "count": total_count,
        "layers": layer_summary,
        "mean_layers": averaged_mean_layers,
    }


@torch.no_grad()
def evaluate(
    model: torch.nn.Module,
    dataset: RulePermutationData,
    args: argparse.Namespace,
    device: torch.device,
    generator: torch.Generator,
    routing_mode: str = "none",
) -> dict[str, Any]:
    was_training = model.training
    model.eval()
    rows = []
    num_experts = int(model.config.moe_num_unique_experts)
    for _ in range(args.eval_batches):
        batch = sample_batch(dataset, args, generator, device)
        forced_ids = forced_ids_from_metadata(batch, args, num_experts, routing_mode)
        if forced_ids is not None:
            set_forced_expert_ids(model, forced_ids)
        else:
            clear_forced_expert_ids(model)
        with autocast_context(args, device):
            output = model(
                batch.source,
                use_cache=False,
                output_attentions=True,
                output_expert_labels=True,
                output_hidden_states=False,
            )
        flat_loss = F.cross_entropy(
            output.logits.reshape(-1, output.logits.shape[-1]).float(),
            batch.target.reshape(-1),
            reduction="none",
        ).reshape_as(batch.target)
        valid_mask = rule_valid_mask(batch.metadata) if args.loss_rule_positions_only else torch.ones_like(batch.target, dtype=torch.bool)
        loss_sum = float(flat_loss[valid_mask].sum().detach().cpu())
        correct = int(((output.logits.argmax(dim=-1) == batch.target) & valid_mask).sum().detach().cpu())
        count = int(valid_mask.sum().detach().cpu())
        row = {"loss_sum": loss_sum, "correct": correct, "count": count, "layers": {}}
        attentions = output.attentions or ()
        expert_labels = getattr(output, "expert_labels", None) or ()
        high_ids = batch.metadata[..., RULE_METADATA_FIELDS["high_unit"]]
        group_ids = batch.metadata[..., RULE_METADATA_FIELDS["rule_group"]]
        rule_ids = batch.metadata[..., RULE_METADATA_FIELDS["rule_id"]]
        rule_mask = rule_valid_mask(batch.metadata)
        for layer_idx in range(len(attentions)):
            layer_labels = expert_labels[layer_idx] if layer_idx < len(expert_labels) else None
            layer_row = {
                "same_high_same_expert": same_feature_same_expert_rate(layer_labels, high_ids, rule_mask),
                "same_rule_group_same_expert": same_feature_same_expert_rate(layer_labels, group_ids, rule_mask),
                "same_rule_id_same_expert": same_feature_same_expert_rate(layer_labels, rule_ids, rule_mask),
                "rule_group_history_mass": attention_history_mass(attentions[layer_idx], group_ids, rule_mask),
                "rule_id_history_mass": attention_history_mass(attentions[layer_idx], rule_ids, rule_mask),
            }
            layer_row.update(expert_load_metrics(layer_labels, num_experts))
            row["layers"][str(layer_idx)] = layer_row
        rows.append(row)
    clear_forced_expert_ids(model)
    if was_training:
        model.train()
    return aggregate_eval_metrics(rows)


