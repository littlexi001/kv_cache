def apply_train_config(args: argparse.Namespace) -> argparse.Namespace:
    if not getattr(args, "load_train_config", False):
        return args
    ckpt_path = Path(args.ckpt_file)
    train_config_path = ckpt_path.parent.parent / "train_config.json"
    if not train_config_path.exists():
        return args
    train_config = json.loads(train_config_path.read_text(encoding="utf-8"))
    protected = {"ckpt_file", "output_path", "load_train_config", "device", "eval_batches"}
    for key, value in train_config.items():
        if key not in protected and hasattr(args, key):
            setattr(args, key, value)
    return args


